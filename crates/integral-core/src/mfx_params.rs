#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn all_68_types_present() {
        assert_eq!(MFX_TYPE_DEFS.len(), 68);
        assert_eq!(MFX_TYPE_NAMES.len(), 68);
    }

    #[test]
    fn thru_has_no_params() {
        let def = mfx_type_def(0).unwrap();
        assert_eq!(def.name, "Thru");
        assert!(def.params.is_empty());
    }

    #[test]
    fn equalizer_has_11_params() {
        let def = mfx_type_def(1).unwrap();
        assert_eq!(def.name, "Equalizer");
        assert_eq!(def.params.len(), 11);
    }

    #[test]
    fn type_ids_match_indices() {
        for (i, def) in MFX_TYPE_DEFS.iter().enumerate() {
            assert_eq!(def.id as usize, i, "type {} id mismatch", def.name);
        }
    }

    #[test]
    fn names_match_defs() {
        for (i, name) in MFX_TYPE_NAMES.iter().enumerate() {
            assert_eq!(*name, MFX_TYPE_DEFS[i].name, "name mismatch at index {i}");
        }
    }

    #[test]
    fn out_of_range_returns_none() {
        assert!(mfx_type_def(68).is_none());
        assert!(mfx_type_def(255).is_none());
    }

    #[test]
    fn param_indices_start_at_1_and_are_sequential() {
        for def in MFX_TYPE_DEFS.iter() {
            if def.params.is_empty() {
                continue;
            }
            assert_eq!(
                def.params[0].index, 1,
                "first param of {} should be 1",
                def.name
            );
            for (i, p) in def.params.iter().enumerate() {
                assert_eq!(
                    p.index as usize,
                    i + 1,
                    "param index mismatch in {}: expected {}, got {}",
                    def.name,
                    i + 1,
                    p.index,
                );
            }
        }
    }
}
